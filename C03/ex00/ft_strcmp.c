int	ft_strcmp(char *s1, char *s2)
{	
	int	answ1;
	int	i;

	i = 0;
	while ((s1[i] != '\0') || (s2[i] != '\0'))
	{
		if (s1[i] == s2[i])
		{
			if (s1[i] == '\0')
			{
				return (0);
			}
		}
		else if (s1[i] != s2[i])
		{
			answ1 = s1[i] - s2[i];
			return (answ1);
		}
		i++;
	}
	return (0);
}
